const Stack = createNativeStackNavigator();
const AnimalNavigator=({route})=>{
    const user = route.params.user;
    return (
        <Stack.Navigator
          initialRouteName="animals"
          screenOptions={
            {
              headerShown: false,
            }
          }
        >
          <Stack.Screen
            name="animals"
            component={Animals}
            initialParams={user}
            options={{ title: "Animals" }}
          />
    
          <Stack.Screen
            name="addAnimal"
            component={AddAnimal}
            options={{ title: "AddAnimal" }}
          />
          <Stack.Screen
            name="Cows"
            component={Cows}
            options={{ title: "Cows" }}
          />
                <Stack.Screen
            name="Sheep"
            component={Sheep}
            options={{ title: "Sheep" }}
          />
                <Stack.Screen
            name="Chicken"
            component={Chicken}
            options={{ title: "Chicken" }}
          />
        </Stack.Navigator>
      );
    };
    export default AnimalNavigator;      
